function wow100(x3) {
    let result
    if (x3<100) {
        result = "меньше"
    } else if (x3>100) {
        result = "больше"
    }

    else if (x3=100) {
        result = "равно"
    }

    else {
        result = `ВНИМАНИЕ, ОШИБКА ПРИ ОБРАБОТКЕ ВВЕДЕННОГО ЗНАЧЕНИЯ ${x3}!`
    }
    return result
}
let x3 = prompt("Задача 3. Введите число для сравнения с числом 100")
console.log(`Задача 3. Пользователь ввел число ${x3}`);
console.log(`Число ${x3} ${wow100(x3)} 100 `);

