function maxOfTwo(n1, n2) {
    let max = Math.max(n1, n2)
    return max
}

let x2_1 = prompt("Задача 2. Введите первое число для сравнения")
let x2_2 = prompt("Задача 2. Введите второе число для сравнения")

console.log(`Задача 2. Пользователь ввел числа ${x2_1} и ${x2_2}`);
console.log(`Наибольшим из двух введенных чисел является ${maxOfTwo(x2_1, x2_2)}`);

